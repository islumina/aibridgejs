#!/usr/bin/env node
// Build llms-full.txt by concatenating the canonical English documentation
// surface so LLM agents can pull complete project context in one fetch.

import { readFile, writeFile } from "node:fs/promises";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const root = resolve(dirname(fileURLToPath(import.meta.url)), "..");

const sections = [
  { path: "README.md", title: null },
  { path: "CHANGELOG.md", title: "Changelog" },
  { path: "CONTRIBUTING.md", title: "Contributing" },
];

const header = `# aibridgejs - full LLM context

This file is auto-generated by \`scripts/build-llms-full.mjs\`. Do not edit
manually; instead edit the underlying source documents and re-run the
script.

The short index lives at \`llms.txt\` (see https://llmstxt.org/).

---
`;

let out = header;

for (const { path, title } of sections) {
  const content = await readFile(resolve(root, path), "utf8");
  if (title) out += `\n# ${title} (\`${path}\`)\n\n`;
  else out += `\n<!-- ===== ${path} ===== -->\n\n`;
  out += content;
  if (!content.endsWith("\n")) out += "\n";
  out += "\n---\n";
}

await writeFile(resolve(root, "llms-full.txt"), out);

const bytes = Buffer.byteLength(out, "utf8");
console.log(`build-llms-full: wrote llms-full.txt (${(bytes / 1024).toFixed(1)} KB).`);
